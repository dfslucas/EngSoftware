package Classes;

/**
 *
 * @author eldi
 */
public class Carro {
    String placa, modelo;
    
    public Carro() {
    }
    public Carro(String placa, String modelo){
        this.placa = placa;
        this.modelo = modelo;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }
    
}
